vector< ll > divisor(ll n) {
  vector< ll > ret;
  for(ll i = 1; i * i <= n; i++) {
    if(n % i == 0) {
      ret.push_back(i);
      if(i * i != n) ret.push_back(n / i);
    }
  }
  sort(begin(ret), end(ret));
  return (ret);
}

int main() {
    ll n;
    cin>>n;
    ll ans = 0;

    auto div = divisor(n);

    ans += n;
    ll itr = 1;
    for (int i = 1; i < div.size(); ++i) {
        if(n / div[i] > 1){
            if(div[i] - div[i - 1] > 1){
                ans += (n / div[i]) * (div[i] - div[i - 1]);
                itr = div[i];
            }else{
                ans += n / div[i];
                itr = div[i];
            }
        }else{
            ans += n - itr;
            break;
        }

    }


    cout<<ans<<endl;
    return 0;
}
